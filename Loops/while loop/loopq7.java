
import java.util.Scanner;
class Loopq7
{

	public static void main(String[] args)
	{
	  Scanner sc=new Scanner(System.in);
	  int f=sc.nextInt();
	  int l=sc.nextInt();
	  int sum=0;
	     
                   
	  while(f<=l)	
				   
		{
	
		 sum+=f;
		 f++;	   
		}
		System.out.println("Sum from 1 to 100 =" +sum);
		 
		
	}

}